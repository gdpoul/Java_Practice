package operators;

public class A2 {

	// Relational Operator
	
	public static void main(String[] args) {
		
		int a=10,b=20,c=30;
		
		System.out.println(b>a);
		System.out.println(a>=c);
		System.out.println(c<b);
		System.out.println(b<=30);
		System.out.println(a==b);
		System.out.println(a!=c);
	}

}

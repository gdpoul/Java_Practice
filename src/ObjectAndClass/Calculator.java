package ObjectAndClass;

public class Calculator {
	
	
	public int add(int a, int b)
	{
		int sum=a+b;
		return sum;
	}
	
	public int substract(int a, int b)
	{
		int sub=a-b;
		return sub;
	}
	
	public int multi(int a, int b)
	{
		int multi=a*b;
		return multi;
		
	}
	public int division(int a, int b)
	{
		int div=a/b;
		return div;
	}
	
	
}



















